x, y = 5, 10
print("x = {0}, y = {1}".format(x, y))
print("x + y = {0}".format(x + y)) # 15
print("x - y = {0}".format(x - y)) # -5
print("x / y = {0}".format(x / y)) # 0.5
print("y % x = {0}".format(10 % 5)) # 0
print("x // y = {0}".format(5 // 10)) # 0
print("x ** y = {0}".format(5 ** 10)) # Whatever 5 to the tenth is
print("absolute value of -x = {0}".format(abs(-x)))
x < y # True
x <= y # True
x > y # False
x >= y # False
x == y # False
x != y # True